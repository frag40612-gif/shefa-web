// الحصول على الأمراض لحرف معين
export function getDiseasesByLetter(lang, letter) {
  const data = lang === 'ar' ? DISEASES_AR : DISEASES_EN
  const list = data[letter] || []
  return list
}

// الحصول على كل الحروف التي لديها أمراض
export function getAvailableLetters(lang) {
  const data = lang === 'ar' ? DISEASES_AR : DISEASES_EN
  return Object.keys(data).sort()
}
